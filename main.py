data = input('Gib einen Satz ein: ')
data = data.lower()
vokale = ['a','e','i','o','u']
vokal = 0

for string in data:
    for i in vokale:
        if string == i:
            vokal += 1

print('Der Satz hat %s Vokale.' %(vokal))
